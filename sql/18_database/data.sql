
-- Create the "users" table
CREATE TABLE users (
    id INT PRIMARY KEY,
    email VARCHAR(255) UNIQUE,
    password VARCHAR(100),
    fullname VARCHAR(255),
    status VARCHAR(50),
    role VARCHAR(50)
);

-- Create the "products" table
CREATE TABLE products (
    id INT PRIMARY KEY,
    name VARCHAR(255),
    description VARCHAR(500),
    price DECIMAL(10,2),
    stock INT,
    original_price DECIMAL(10,2),
    category VARCHAR(100)
);

-- Create the "carts" table (each cart belongs to a user)
CREATE TABLE carts (
    id INT PRIMARY KEY,
    usersid INT,
    FOREIGN KEY (usersid) REFERENCES users(id)
);

-- Create the "orders" table (each order is placed by a user)
CREATE TABLE orders (
    id INT PRIMARY KEY,
    usersid INT,
    description VARCHAR(500),
    status VARCHAR(50),
    total_amount DECIMAL(10,2),
    FOREIGN KEY (usersid) REFERENCES users(id)
);

-- Create the "cart_items" table (items in a cart reference both a cart and a product)
CREATE TABLE cart_items (
    id INT PRIMARY KEY,
    quantity INT,
    cart_id INT,
    products_id INT,
    FOREIGN KEY (cart_id) REFERENCES carts(id),
    FOREIGN KEY (products_id) REFERENCES products(id)
);

-- Create the "orders_products" table (each orderâ€™s product details)
CREATE TABLE orders_products (
    id INT PRIMARY KEY,
    ordersid INT,
    productsid INT,
    original_price DECIMAL(10,2),
    price DECIMAL(10,2),
    FOREIGN KEY (ordersid) REFERENCES orders(id),
    FOREIGN KEY (productsid) REFERENCES products(id)
);

