public class HW04 {
    public static void main(String[] args) {
        int a = 2, b = 10;

        if (a > b) {
            System.out.printf("%d lon hon %d", a, b);
        } else if (a < b) {
            System.out.printf("\n %d nho hon %d", a, b);
        } else {
            System.out.println("Hai so bang nhau");
        }
    }
}
